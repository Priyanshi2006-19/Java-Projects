// Swapping

public class first_3 {

    public static void main(String[] args) {
        int a=12;
        int b=13;
        int temp=0;
        System.out.println("Value of numbers before swapping : a="+a+",b="+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("Value of numbers before swapping : a="+a+",b="+b);
    }
}


